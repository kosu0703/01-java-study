package com.ict.day02;

public class Hw03 {
	public static void main(String[] args) {
		byte b1 = 100 ;
		System.out.println(b1);
		
		b1 = 127 ;
		System.out.println(b1);
	
		// byte -128 ~ 127
	
		int su = 35 ; 
		System.out.println(su);
		
		long num = 32L ;
		// L  l  1
		System.out.println(num);
		
	}
}
