package com.ict.day02;

public class Hw08 {
	public static void main(String[] args) {
		int su1 = 9 ;
		int su2 = 4 ;
		int res = 0 ;
		
		
		res = su1 + su2 ;
		System.out.println("결과 : " + res);
		System.out.println("결과 : " + su1 + " + " + su2 + " = " + res);
		
		res = su1 - su2 ;
		System.out.println("결과 : " + res);
		System.out.println("결과 : " + su1 + " - " + su2 + " = " + res);
		
		res = su1 * su2 ;
		System.out.println("결과 : " + res);
		System.out.println("결과 : " + su1 + " * " + su2 + " = " + res);
		
		res = su1 / su2 ;
		System.out.println("결과 : " + res);
		System.out.println("결과 : " + su1 + " / " + su2 + " = " + res);
		
		double res2 = su1 / su2 ;
		System.out.println("결과 : " + res2);
		
		double res3 = su1 * 1.0 / su2 ;
		System.out.println("결과 : " + res3);
		
		
		res = su1 % su2 ;
		System.out.println("결과 : " + res);
		System.out.println("결과 : " + su1 + " % " + su2 + " = " + res);
		
	
	
	}
}
